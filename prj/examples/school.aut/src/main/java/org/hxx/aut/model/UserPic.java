package org.hxx.aut.model;

public class UserPic {
	private String phoneNum;
	private String originPicPath;
	private String compressPicPath;
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}
	public String getOriginPicPath() {
		return originPicPath;
	}
	public void setOriginPicPath(String originPicPath) {
		this.originPicPath = originPicPath;
	}
	public String getCompressPicPath() {
		return compressPicPath;
	}
	public void setCompressPicPath(String compressPicPath) {
		this.compressPicPath = compressPicPath;
	}
	
}
