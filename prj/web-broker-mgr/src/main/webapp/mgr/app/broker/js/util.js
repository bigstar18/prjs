function setReadOnly(obj)
{
		  obj.readOnly = true;
		  obj.style.backgroundColor = "#C0C0C0";
}

function setReadWrite(obj)
{
		  obj.readOnly = false;
		  obj.style.backgroundColor = "white";
}

function setDisabled(obj)
{
		  obj.disabled = true;
		  obj.style.backgroundColor = "#C0C0C0";
}

function setEnabled(obj)
{
		  obj.disabled = false;
		  obj.style.backgroundColor = "white";
}
		
